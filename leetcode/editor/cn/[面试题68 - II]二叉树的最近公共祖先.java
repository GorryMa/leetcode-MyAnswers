/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
class Solution {
    /**
     * 方法一：递归
     * 分情况讨论
     * 1.若p或者q其中一个为根节点  则最近公共祖先显然为根节点
     * 2.p,q均不为根节点，且分别位于根节点左右两个子树    此时左右两个子树的递归最近公共祖先均为null
     * 3.p,q均不为根节点且位于根节点的同一侧子树上       此时左右两个子树的递归最近公共祖先一个为null，一个不为null
     * 若p或q其中一个为另一个的祖先节点，则最近公共祖先为该节点
     * 否则，从根节点开始左子树或者和右子树遍历，当遍历值等于p或q或者null时，返回
     * @param root
     * @param p
     * @param q
     * @return
     */
    public TreeNode lowestCommonAncestor(TreeNode root, TreeNode p, TreeNode q) {
        if (root == null) {
            return null;
        }
        //情况1
        if (p==root||q==root){
            return root;
        }
        TreeNode left = lowestCommonAncestor(root.left, p, q);
        TreeNode right = lowestCommonAncestor(root.right, p, q);
        if(left == null && right == null) return null; // 1.
        if(left == null) return right; // 3.
        if(right == null) return left; // 4.
        return root; // 2. if(left != null and right != null)
    }
}
